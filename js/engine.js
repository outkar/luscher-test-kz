// Кітап (Драгунский): компенсация белгілері тек сұр/қара/қоңыр үшін
// қойылады — фиолетов пен негізгі түстер үшін емес.
const AUX = new Set([0,6,7]);
const BASIC = new Set([1,2,3,4]);

// Мазасыздық: алдыңғы 1-3 орындағы қосалқы түстер + соңғы 6-8 орындағы негізгі түстер
function computeAnxiety(row){
  let a=0;
  for(let i=0;i<3;i++){ if(AUX.has(row[i]))   a += (3-i); } // 1→3, 2→2, 3→1
  for(let i=5;i<8;i++){ if(BASIC.has(row[i])) a += (i-4); } // 6→1, 7→2, 8→3
  return a; // 0..12
}

// Функционалдық топтар: (1,2)+ (3,4)× (5,6)= (7,8)−
function functionalGroups(row){
  return {
    plus:[row[0],row[1]], cross:[row[2],row[3]],
    equal:[row[4],row[5]], minus:[row[6],row[7]],
  };
}
